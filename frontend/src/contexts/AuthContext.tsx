import React, { createContext, useContext, useEffect, useState } from 'react';
import { User, AuthContextType, ApiResponse, AuthResponse } from '../types';
import api from '../lib/api';
import { toast } from '../components/ui/Toast';

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};

interface AuthProviderProps {
  children: React.ReactNode;
}

export const AuthProvider: React.FC<AuthProviderProps> = ({ children }) => {
  const [user, setUser] = useState<User | null>(null);
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    const initAuth = async () => {
      const token = localStorage.getItem('token');
      const savedUser = localStorage.getItem('user');
      
      if (token && savedUser) {
        try {
          // First set the user from localStorage to avoid loading state
          const userData = JSON.parse(savedUser);
          setUser(userData);
          
          // Then verify token is still valid by fetching current user
          const response = await api.get<ApiResponse<User>>('/auth/me');
          setUser(response.data.data);
        } catch (error) {
          // If token validation fails, clear stored data but don't redirect
          // The redirect will be handled by the API interceptor for auth endpoints only
          localStorage.removeItem('token');
          localStorage.removeItem('user');
          setUser(null);
        }
      }
      setIsLoading(false);
    };

    initAuth();
  }, []);

  const login = async (email: string, password: string) => {
    try {
      const response = await api.post<ApiResponse<AuthResponse>>('/auth/login', { email, password });
      const { token, user: userData } = response.data.data;
      
      localStorage.setItem('token', token);
      localStorage.setItem('user', JSON.stringify(userData));
      setUser(userData);
      toast.success('Login successful!');
    } catch (error: any) {
      const message = error.response?.data?.message || 'Login failed';
      toast.error(message);
      throw error;
    }
  };

  const register = async (username: string, email: string, password: string) => {
    try {
      const response = await api.post<ApiResponse<AuthResponse>>('/auth/register', { 
        username, 
        email, 
        password 
      });
      const { token, user: userData } = response.data.data;
      
      localStorage.setItem('token', token);
      localStorage.setItem('user', JSON.stringify(userData));
      setUser(userData);
      toast.success('Registration successful!');
    } catch (error: any) {
      const message = error.response?.data?.message || 'Registration failed';
      toast.error(message);
      throw error;
    }
  };

  const logout = () => {
    localStorage.removeItem('token');
    localStorage.removeItem('user');
    setUser(null);
    
    // Clear all React Query cache when logging out
    // This ensures no cached user-specific data persists
    toast.success('Logged out successfully');
    
    // Force page reload to clear any cached state
    window.location.href = '/';
  };

  const value: AuthContextType = {
    user,
    login,
    register,
    logout,
    isLoading,
  };

  return <AuthContext.Provider value={value}>{children}</AuthContext.Provider>;
};